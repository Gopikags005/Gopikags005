# Initialize a Timer object
buzz_timer = Timer()

def buzzer_freq():
    global freq_buzzer_active
    global buzz_timer
    buzz_time = 3000  # Initial buzz time in milliseconds
    buzz_increment = 3000  # Increment value for buzz time
   
    if not freq_buzzer_active:
        freq_buzzer_active = True
        print("Frequency buzzer started")
        silence_time = 1000  # Silence time in milliseconds
        buzz_count = 5  # Number of buzzes in the pattern
        buzz_index = 0  # Index to keep track of the current buzz count

        def buzz_callback(timer):
            nonlocal buzz_index, buzz_time
            if buzz_index < buzz_count * 2:  # Multiply by 2 to account for silence
                if buzz_index % 2 == 0:  # Buzz
                    buzzer_2.value(1)
                    buzz_timer.init(period=buzz_time, mode=Timer.ONE_SHOT, callback=buzz_callback)
                else:  # Silence
                    buzzer_2.value(0)
                    buzz_timer.init(period=silence_time, mode=Timer.ONE_SHOT, callback=buzz_callback)
                buzz_index += 1
            else:
                # Stop buzzer after completing the pattern
                buzz_timer.deinit()
                freq_buzzer_active = False
                print("Frequency buzzer stopped")
       
        # Start the first buzz cycle
        buzz_callback(buzz_timer)
        buzz_time += buzz_increment  # Increase buzz time for next cycle
    else:
        # If the frequency buzzer is already active, stop it
        buzz_timer.deinit()
        freq_buzzer_active = False
        buzzer_2.value(0)
        print("Frequency buzzer stopped")

